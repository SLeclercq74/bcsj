            <p><span class="emphasys">ARTICLE 10 : </span> Le/la joueur/joueuse doit être dans une tenue compatible avec la
                pratique du basketball. Il/Elle ne pourra disputer de rencontres s'il/elle n'est pas muni(e) de la tenue
                réglementaire en match, de la chasuble d’entrainement fournie lors de son inscription initiale au club et en
                particulier des chaussures adéquates. </p>

            <p><span class="emphasys">ARTICLE 11 : </span> Les parents des joueurs/joueuses mineur(e)s et les
                joueurs/joueuses
                majeur(e)s ont la charge de l’entretien des tenues de match (maillots et short) et des gobelets. Ils doivent
                organiser un roulement et laver à tour de rôle toutes les tenues en même temps et les ranger dans le sac
                fourni à cet effet. Le sac doit être déposé au local avant le prochain match.

                Un inventaire est effectué par le club en fin de saison.</p>

            <p><span class="emphasys">ARTICLE 12 : </span> A domicile, les parents des joueurs/joueuses mineur(e)s et les
                joueurs/joueuses majeur(e)s ont la charge d’organiser une collation d’après match.</p>

            <h3>C - L'ENTRAINEMENT</h3>

            <p><span class="emphasys">ARTICLE 13 : </span> Les jours et horaires des entrainements sont fournis en début
                d’année pour toutes les catégories et sont consultables sur le site Internet du club et les réseaux sociaux.
            </p>

            <p><span class="emphasys">ARTICLE 14 : </span> Le/La joueur/joueuse doit arriver au minimum 10 minutes avant
                pour se mettre en tenue. Il/Elle devra venir à l’entraînement avec un short ou un jogging, sa chasuble
                d’entrainement (ou un t-shirt, mais l’oubli répété pourra être passible de sanctions) et en particulier des
                chaussures de sport intérieur adéquates, bijoux retirés et cheveux longs attachés. S’il y a risque de
                blessure, l’entraineur/euse refusera l’accès à l’entraînement. </p>

            <p><span class="emphasys">ARTICLE 15 : </span> Tous les joueurs/joueuses doivent aider à installer et ranger le
                matériel d'entraînement. </p>

            <p><span class="emphasys">ARTICLE 16 : </span> Le/La joueur/joueuse est responsable de ses propres affaires.
                Le/La joueur/joueuse ne doit rien laisser dans les vestiaires. Les objets de valeur (bijoux, téléphones
                portables et autres) doivent rester au domicile du joueur, le club n'étant en aucun cas tenu responsable des
                vols commis. </p>

            <h3>D - LES SANCTIONS</h3>

            <p><span class="emphasys">ARTICLE 17 : </span>Les sanctions en cours de matchs (fautes antisportives) ne sont
                excusables que pour des faits de jeu.

                Tout autre fait, tel que :

                Les fautes disqualifiantes : coup, tentative de coup, insultes graves, …

                Les fautes techniques : contestation des décisions d'arbitres, insultes, mauvais comportement, crachat, etc.
                seront sanctionnés au sein du club par la commission de discipline qui se sera constituée selon nos statuts
                après avis de la commission de discipline de la Ligue AURA.

                Le/la joueur/joueuse majeur (e) fautif/ve ou l’entraineur/euse majeur (e) fautif/ve réglera le montant de
                l'amende fixée par la commission de discipline et/ou les dispositions financières de l’année en cours (liste
                en pièce jointe).

                Le/la joueur/joueuse mineur (e) fautif/ve ou l’entraineur/euse mineur (e) fautif/ve devra effectuer des
                missions de sanction disciplinaires dictées par la commission de discipline formée par le club du type :
                arbitrage de match, tenue de table de marque, aide aux entrainements (liste non exhaustive).

                Il/Elle ne pourra participer à une rencontre qu'après règlement de ladite amende ou après avoir effectué la
                sanction disciplinaire. </p>

            <p><span class="emphasys">ARTICLE 18 : </span>Seul l’entraîneur/euse dispose d’un seul recours pour une faute
                technique qu’il/elle estime injustifiée. La commission de discipline du club enquêtera et statuera sur le
                recours.</p>

            <p><span class="emphasys">ARTICLE 19 : </span>L'entraîneur/euse est autorisé à exclure tout joueur lui manquant
                de respect ou perturbant le bon déroulement de son entrainement ou du match (retard, attitude, etc.) </p>

            <p><span class="emphasys">ARTICLE 20 : </span>Les membres du bureau sont les seuls habilités à statuer sur
                l'exclusion d'un(e) licencié(e) qui n'aurait pas respecté le présent règlement. </p>

            <h3>E - L'ASSURANCE</h3>
            <p><span class="emphasys">ARTICLE 21 : </span>L'association souscrit à un contrat d'assurance "responsabilité
                civile" pour toute manifestation sportive ou extra sportive. Par souscription individuelle à une assurance
                complémentaire, par le biais de la licence, les joueurs peuvent bénéficier de garanties "responsabilités
                civiles et individuelles accidents". </p>

