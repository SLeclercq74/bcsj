            <h1>PARTIE II – LE FONCTIONNEMENT DU CLUB</h1>
            <p><span class="emphasys">ARTICLE 1 :</span> Le présent règlement sportif est à disposition de chaque
                licencié(e). Le/la licencié(e) s'engage
                à l'avoir lu et approuvé. </p>

            <p><span class="emphasys">ARTICLE 2 :</span> Tout licencié du club (joueur/joueuse, dirigeant(e),
                entraîneur/euse) a la charge de respecter et
                faire respecter le présent règlement : respecter les horaires de convocation (entraînement, match et
                réunion), avoir un rôle utile le jour du match et être disponible de manière régulière au cours de la
                saison. </p>

            <h3>A - L'INSCRIPTION</h3>

            <p><span class="emphasys">ARTICLE 3 :</span> Ne seront admis à participer à une compétition officielle celles et
                ceux qui seront en règle au point de vue qualification*.

                Ne seront admis à participer aux entrainements que celles et ceux qui seront en règle au point de vue
                qualification* à partir de la 3ème semaine d’entraînement.

                * cotisation payée et licence validée par le comité départemental.</p>

            <p><span class="emphasys">ARTICLE 4 :</span> L'encaissement de la cotisation peut se faire en plusieurs fois, à
                la seule condition que le montant soit déposé au moment de l'inscription au moyen de plusieurs chèques (le
                club se charge de différer les encaissements), ou choix d’un paiement CB en 3 fois sur le site Hello Asso.
            </p>

            <p><span class="emphasys">ARTICLE 5 :</span> Pour son inscription, le joueur/joueuse s'engage à être présent à
                tous les entraînements et à tous les matchs où il aura été convoqué. Si pour des raisons exceptionnelles
                (travail, maladie) le joueur/joueuse est absent, il est tenu d'en avertir au préalable l'entraîneur.</p>


            <h3>B - LES MATCHS</h3>

            <p><span class="emphasys">ARTICLE 6 :</span> Les joueurs/joueuses doivent accepter la concurrence, aucun
                joueur/joueuse n'est titulaire d'office. Tous les critères de concurrence seront connus et seul le critère
                entrainement peut être débattu avant le début de saison. </p>

            <p><span class="emphasys">ARTICLE 7 :</span> Les matchs à domicile : tous les joueurs/joueuses devront se
                présenter une heure avant le début du match. Plusieurs retards du même joueur/joueuse entraîneront une
                sanction, en accord avec l'entraineur et le bureau. </p>

            <p><span class="emphasys">ARTICLE 8 :</span> Le transport pour les matchs en déplacement se fait en voitures
                particulières des parents des joueurs/joueuses ou des joueurs/joueuses majeurs en possession du permis de
                conduire. Il est formellement interdit de prendre dans sa voiture plus de personnes que la loi et/ou
                l'assurance du véhicule ne le prévoit.

                Chaque parent doit, dans la mesure du possible, participer à l’effort collectif pour emmener les
                joueurs/joueuses aux matchs à l’extérieur.

                L’entraîneur doit être véhiculé par les parents. </p>

            <p><span class="emphasys">ARTICLE 9 :</span>  L’utilisation du minibus du club est prioritaire pour l’équipe qui
                se déplace le plus loin et qui en fait la demande au/à la responsable technique. Les frais liés au carburant
                et aux péages sont à la charge :
            <ul>
                <li>des utilisateurs si le déplacement a lieu à l’intérieur du département de la Haute-Savoie, </li>
                <li>du club si le déplacement a lieu à l’extérieur du département de la Haute-Savoie. . </li>
            </ul>
            </p>
            <p>Le minibus est prêté propre avec le plein de carburant (gasoil) et doit être rendu propre avec le plein de
                carburant. Si l’utilisateur/trice constate un manquement de ces consignes à la prise en main du véhicule,
                il/elle doit en informer le/la responsable technique immédiatement. Aucun recours ne sera pris en
                considération a posteriori. </p>
            <p>Seuls les entraineurs et les membres du comité ont le droit de conduire le minibus. Le conducteur/trice doit
                posséder un permis de conduire et une assurance automobile personnelle en cours de validité. </p>
            <p>Le/la conducteur/trice est responsable de sa conduite, il/elle doit respecter le code de la route et
                s’adapter aux conditions de circulation. Il/elle est responsable de s’assurer que le véhicule soit conforme
                aux exigences de sécurité. Le club ne saura être tenu responsable si le/la conducteur/trice a pris le
                véhicule sans effectuer les vérifications d’usage inscrites au code de la route 2023 :
            <ul>
                <li>Que les pneumatiques soient tous suffisamment gonflés et en bon état </li>
                <li>Que le moteur ne fuit pas </li>
                <li>Que les plaques d’immatriculation ainsi que les différents feux soient propres et fonctionnels </li>
                <li>Que les vitres soient propres et que les essuie-glaces soient en état de fonctionne</li>
                <li>Si la carrosserie a été récemment endommagé</li>
                <li>Vérifier le kit de sécurité, à savoir le triangle de pré-signalisation ainsi que le gilet rétro
                    réfléchissant. </li>
                <li>Vérifier le type de carburant qu’utilise le véhicule pour fonctionner, afin de s’assurer qu’il ne risque
                    pas d’abîmer la mécanique en cas d’arrêt dans une station-service. </li>
                <li>S’installer correctement sur le siège conducteur et doit vérifier en détail où se trouve chacune des
                    commandes, pour ne pas se faire surprendre une fois au volant. </li>
                <li>Avant l’allumage du moteur, s’assurer que tous les occupants du véhicule, ses passagers comme lui-même,
                    soient correctement attachés. </li>
                <li>Vérifier que la boîte de vitesses se trouve bien au point mort.</li>
                <li>Une fois le moteur allumé, contrôler le fait qu'aucun voyant d’alerte ne reste allumé. Si l’un des
                    voyants s'active au moment du départ, agir en fonction des besoins exprimés par le véhicule. Qu’il
                    s’agisse d’un simple oubli de ceinture de sécurité ou d’un problème d’huile moteur, il faut
                    absolument trouver une solution à tous les problèmes mis en avant sur le tableau de bord avant de
                    pouvoir s’en aller. </li>
            </ul>
            <span class="emphasys">En cas d’urgence, prévenir la présidence du club et/ou le responsable technique. </span>
